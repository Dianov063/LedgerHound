/**
 * Generates lib/email-templates/templates.ts from the human-editable .md
 * source files. The generated TS embeds each template's content as a string
 * constant so it is GUARANTEED to be bundled into Vercel serverless functions
 * (runtime fs reads of non-imported .md files are NOT reliably traced).
 *
 * Single source of truth = the .md files. After editing any .md, re-run:
 *   node scripts/build-email-templates.cjs
 * (scripts/smoke-test-email-templates.tsx asserts the .ts matches the .md.)
 */
const fs = require('fs');
const path = require('path');

const DIR = path.join(__dirname, '..', 'lib', 'email-templates');

// filename (as the customer receives it) → short id + human label.
// `country: null` = universal (any LATAM Spanish victim); a country code means
// the template is only attached when that country was selected at checkout.
const TEMPLATES = [
  { id: 'binance', filename: 'binance-compliance-es.md', label: 'Binance — Cumplimiento', country: null },
  { id: 'tether', filename: 'tether-legal-es.md', label: 'Tether — Equipo Legal', country: null },
  { id: 'divindat', filename: 'divindat-denuncia-es.md', label: 'DIVINDAT — Denuncia (Perú)', country: 'PE' },
  // TODO future: { id: 'mx-policia', filename: 'policia-cibernetica-es.md', country: 'MX' }
  // TODO future: { id: 'co-dijin', filename: 'dijin-es.md', country: 'CO' }
];

const entries = TEMPLATES.map((t) => {
  const content = fs.readFileSync(path.join(DIR, t.filename), 'utf8');
  return { id: t.id, filename: t.filename, label: t.label, country: t.country, content };
});

const header = `/**
 * AUTO-GENERATED by scripts/build-email-templates.cjs — DO NOT EDIT BY HAND.
 * Source of truth: the sibling .md files. Re-generate after editing them:
 *   node scripts/build-email-templates.cjs
 *
 * Content is embedded as string constants (not fs-read at runtime) so it is
 * always bundled into Vercel serverless functions.
 */

export interface EmailTemplate {
  /** Short id. */
  id: string;
  /** Filename the customer receives the attachment as. */
  filename: string;
  /** Human-readable label (for logs / UI). */
  label: string;
  /**
   * Country gate. \`null\` = universal (attached to any es report).
   * A country code (e.g. 'PE') = only attached when that country was selected.
   */
  country: string | null;
  /** Full markdown content. */
  content: string;
}
`;

const body = `
/** Spanish recovery email/letter templates, attached to es-locale reports. */
export const EMAIL_TEMPLATES_ES: EmailTemplate[] = ${JSON.stringify(entries, null, 2)};
`;

fs.writeFileSync(path.join(DIR, 'templates.ts'), header + body, 'utf8');
console.log(`Wrote lib/email-templates/templates.ts (${entries.length} templates, ${entries.reduce((n, e) => n + e.content.length, 0)} chars).`);
